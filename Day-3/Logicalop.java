
public class Logicalop {

    public static void main(String[] args) {
        System.out.println((10 > 2) && (4 > 2));// Logical operator and && Ans = True
        System.out.println((10 > 2) && (4 < 2));// Logical operator and && Ans = False
        System.out.println((10 > 2) || (4 < 2));// Logical operator OR || Ans = True
        System.out.println((10 < 2) || (4 < 2));// Logical operator OR || Ans = False
        System.out.println((20)!=(10));
    }
}
