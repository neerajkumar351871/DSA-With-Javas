
public class Findlargesttheenumber {

    public static void main(String[] args) {
        int a = 2, b = 100, c = 30;
        if ((a >= b) && (a >= c)) {
            System.out.println("A is greater:" + a);
        } else if (b >= c) {
            System.out.println("B is greater:" + b);
        } else {
            System.out.println("C is greater:" + c);
        }
    }
}
